const supportedLanguages = ['en', 'ko', 'vi', 'th']; // Added 'th'

function setLanguage(lang) {
    document.documentElement.lang = lang;
    localStorage.setItem('preferredLanguage', lang); // Save preference

    translationKeys.forEach(key => {
        const elements = document.querySelectorAll(`[data-translate="${key}"]`);
        elements.forEach(element => {
            if (translations[lang][key]) {
                // Use innerHTML for keys that contain HTML tags like <strong>
                // Add new keys with HTML content here
                const keysWithHTML = [
                    'heroSubtitle', 'challengesP2', 'challengesBox2Li1', 'challengesBox2Li5',
                    'feature1Desc', 'feature2Desc', 'tier1Feature1', 'tier2Feature1', 'tier2Feature2'
                ];
                if (keysWithHTML.includes(key)) {
                     element.innerHTML = translations[lang][key];
                } else {
                    element.textContent = translations[lang][key];
                }
            }
        });
        // Special case for page title
        if (key === 'pageTitle') {
            document.title = translations[lang][key];
        }
    });


    // Update language button texts and active state
    const langButtons = document.querySelectorAll('.lang-button');
    langButtons.forEach(button => {
        const buttonLang = button.getAttribute('data-lang');
        // const buttonKey = `lang${buttonLang.charAt(0).toUpperCase() + buttonLang.slice(1)}`;
        // if (translations[lang][buttonKey]) {
        //      button.textContent = translations[lang][buttonKey]; // Commented out: Buttons now use emojis
        // }
         // Highlight active language
        if (buttonLang === lang) {
            button.classList.add('active');
        } else {
            button.classList.remove('active');
        }
    });
}

// Function to get the best match for browser language
function getBrowserLanguage() {
    const browserLangs = navigator.languages || [navigator.language || navigator.userLanguage];
    for (const lang of browserLangs) {
        const baseLang = lang.split('-')[0].toLowerCase(); // Get base language (e.g., 'en' from 'en-US')
        if (supportedLanguages.includes(baseLang)) {
            return baseLang;
        }
    }
    return 'vi'; // Default to Vietnamese if no match found
}


